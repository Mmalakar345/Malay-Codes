# String Operators and String methods
name="shravana"
print("Name is=",name)
# Slicing
print("name[0:8:2]=",name[0:8:2])
print("name[1::]=",name[1::])
print("name[:4]=",name[:4])
print("name[0:8]=",name[0:8])
print("name[0:]=",name[0:])
print("name[-1:-9:-1]=",name[-1:-9:-1])
print("name[:len(name):2]=",name[:len(name):2])
print("name[3:3]=",name[3:3])
print("name[3:10]=",name[3:10])
