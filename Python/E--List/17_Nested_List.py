########################### Introduction to nested list  #########################

### Uneven size nested list

li=[[1,5],[5,6,9],[8,7,2,4]]
print(li[0])
print(li[1])
print(li[2])

li=[[1,5],[5,6,9],[8,7,2,4]]
print(li[0][0],li[0][1])
print(li[1][0],li[1][1],li[1][2])
print(li[2][0],li[2][1],li[2][2],li[2][3])