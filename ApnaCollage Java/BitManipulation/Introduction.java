// Write a java code to show all the bitwise operator.
public class Introduction {
    public static void main(String[] args) {
        System.out.println("Binary AND => "+(6 & 1));
        System.out.println("Binary OR => "+(5 | 6));
        System.out.println("Binary XOR => "+(5 ^ 1));
        System.out.println("Binary One's Compliment => "+(~5));
        System.out.println("Binary left Shift => "+(5 << 1));
        System.out.println("Binary Right Shift => "+(5 >> 2));
       
    }
    
}
